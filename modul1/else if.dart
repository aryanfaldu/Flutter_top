void main ()
{
  int marks = 39;

  if (marks>=90)
    {
      print('A+');
    }
  else if (marks>=80)
  {
    print('A');
  }
 else if (marks>=70)
  {
    print('B+');
  }
  else if (marks>=60)
  {
    print('B');
  }
  else if (marks>=50)
  {
    print('C');
  }
  else if (marks>=40)
  {
    print('D');
  }
  else
  {
    print('Fail');
  }
}
